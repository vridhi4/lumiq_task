def main():
    # Get input from the user
    num1 = float(input("Enter the first number: "))
    num2 = float(input("Enter the second number: "))

    # Calculate the sum
    total = num1 + num2

    # Print the sum
    print("The sum of", num1, "and", num2, "is:", total)

if __name__ == "__main__":
    main()

